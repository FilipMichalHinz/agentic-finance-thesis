GRANT ALL ON TABLE public.stock_news_daily TO anon;
GRANT ALL ON TABLE public.stock_news_daily TO authenticated;
GRANT ALL ON TABLE public.stock_news_daily TO service_role;
GRANT ALL ON TABLE public.press_releases_daily TO anon;
GRANT ALL ON TABLE public.press_releases_daily TO authenticated;
GRANT ALL ON TABLE public.press_releases_daily TO service_role;

