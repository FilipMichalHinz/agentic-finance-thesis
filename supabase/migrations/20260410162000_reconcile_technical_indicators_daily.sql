GRANT ALL ON SEQUENCE public.technical_indicators_daily_id_seq TO anon;
GRANT ALL ON SEQUENCE public.technical_indicators_daily_id_seq TO authenticated;
GRANT ALL ON SEQUENCE public.technical_indicators_daily_id_seq TO service_role;
